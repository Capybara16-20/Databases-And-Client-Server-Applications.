CREATE VIEW [Абонементы]
AS
SELECT Абонемент.ID, 
Клиент.ФИО AS Клиент, 
[Вид абонемента].Название AS [Вид абонемента], 
[Количество индивидуальных занятий], 
Стоимость
FROM (Абонемент JOIN Клиент ON Абонемент.Клиент = Клиент.ID)
JOIN [Вид абонемента] ON Абонемент.[Вид абонемента] = [Вид абонемента].ID